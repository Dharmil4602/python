## 2. Assignment Operator

x = 2 ## Equals Operator
print(x)
x += 4 ## This is same as x = x + 4
print(x)
x -= 2 ## This is same as x = x - 2
print(x)
x *= 2 ## This Is Same As x = x * 2
print(x)
x /= 2 ## This Is Same As x = x / 2
print(x)
x %= 2 ## This Is Same As x = x % 2
print(x)
x **= 2 ## This Is Same As x = x ** 2
print(x)